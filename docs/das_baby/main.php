<h2>Beschreibung</h2>
<p>
Das Baby ist eine sehr alte Geschichte von mir, die ich vor vielen Jahren
einmal begonnen habe auf einer Autofahrt in einen Urlaub. In folgenden Urlauben
und zu sonstigen Gelegenheiten habe ich immer ein wenig die Geschichte
erweitert.
</p>
<p>
Sie beschreibt das Leben aus der Sicht eines kreativen Babies.
</p>

