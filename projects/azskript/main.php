<b>Beschreibung</b><br>
Hierbei handelt es sich um einen vollstaendigen Skript-Interpreter, 
welcher fuer jeden beliebigen Zweck eingesetzt werden kann. Das Einbauen 
in ein vorhandenes Programm ist dabei sehr einfach und es koennen dabei 
auch problemlos neue Funktionen hinzugefuegt werden.<br>
Als Beispiel fuer die Anwendung dieses Skriptes sind die 2 Programme <a 
href="../sabo_racer">SABO Racer</a> und <a href="../azgames">AZGames</a> 
(der GameServer aus dem Bundle) zu nennen.
</p>

