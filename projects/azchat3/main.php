<p>
<b>Beschreibung</b><br>
Hier handelt es sich um ein P2P-System aehnlich wie eDonkey. Aehnlich 
wie bei eDonkey ist man nicht festgelegt auf einen Server, sondern eine 
eigene Auswahl ist moeglich. Die Benutzeroberflaeche des Programms 
selbst ist vollkommen dynamisch und kann serverseitig angepasst werden. 
