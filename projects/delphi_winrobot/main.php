<p>
<b>Entstehung</b><br>
Dieses Programm entstand als ein Schul-Projekt in Delphi.
</p>

<p>
<b>Beschreibung</b><br>
Es handelt sich um eine Multiplayer-Spiele-Engine, d. h. dieses Programm 
enthaellt eigentlich nur das Protokoll und ein kleines Beispiel zur 
Demonstration. Das Protokoll baut auf HTTP auf und funktioniert daher 
auch ueber HTTP-Proxy-Server.<br>
Das Programm ist im Prinzip eine stark vereinfachte Version von 
<a href="../azgames">AZGames</a>.
</p>

<p>
<b>Name</b><br>
Der Name des Projektes enthaellt einmal die Information, dass das 
Programm in Delphi geschrieben wurde (ist eine Besonderheit fuer mich, 
da ich nicht haeufig in Delphi programmiere) und zusaetzlich den 
eigentlichen Namen winRobot. Dieser ist eine Anlehnung an das Spiel <a 
href="../robot">Robot</a>, welches ich als Multiplayer-Spiel eigentlich 
urspruenglich bei diesem Schul-Projekt umsetzen wollte, allerdings 
reichte die Zeit dafuer nicht.
</p>

<p>
<b>Quell-Code</b><br>
Der Quell-Code des Programmes ist im Prinzip portiert von Visual Basic, 
d. h. von dem Art Framework, welches ich mir im Laufe der Zeit fuer 
Visual Basic zusammenprogrammiert habe.
</p>

<p>
<b>Hinweise</b><br>
Ich habe das Programm erfolgreich ohne Probleme mit Wine zum Laufen 
bekommen. Jediglich sollte man beachten, dass man den Port aendern muss, 
wenn das Programm nicht mit root-Rechten laueft, denn der 
voreingestellte Port ist zu niedrig. Das gleiche gillt auch fuer 
Windows.
</p>

