<p>
<b>Beschreibung</b><br>
Bei diesem Spiel handelt es sich um ein simples 
Drueck-den-Knopf-um-zu-schiessen-Spiel, welches gerade mal eine 
1-dimensionale Bewegungsfreiheit auf einem 2-dimensionalen 
eingeschraenkten Spielfeld zulaesst, das gleiche gilt fuer die am 
anderen Ende des Spielfeldes sich befindenden und voellig sinnfreie 
Bewegungen ausfuehrenden "Gegner", welche allerdings nicht sehr viel 
mehr tun als einfach da zu sein.
</p>

