<p>
<b>Beschreibung</b><br>
Bei diesem Spiel handelt es sich um eine Version des bekannten 
Snake-Spiels mit insgesamt 16 Levels. Eine weitere Besonderheit ist der 
Zeitdruck, der durch einen Timer entsteht.
</p>

<p>
<b>Code</b><br>
Es handelt sich hier um eines der aeltesten Spiele von mir, eines der 
ersten Programme, die ich geschrieben habe. Da das ganze damals noch 
unter Windows 3.1 war, wurde die erste Version in Visual Basic 3 
geschrieben und ist auch unter Windows 3.1 lauffaehig (dies ist die 16 
Bit Version des Programms). Spaeter habe ich das Spiel nach einer 
hoeheren Visual Basic Version in 32 Bit portiert, so dass die Windows 9x 
Version entstand. Die 16 Bit Version sollte unter Windows 9x natuerlich 
auch lauffaehig sein.
</p>

<p>
<b>(Linux) Wine</b><br>
Die 16 Bit Version laueft ohne Probleme unter Wine und zwar sehr gut und 
fluessig, bei der 32 Bit Version habe ich dieselben Probleme wie bei dem 
Spiel <a href="../robot">Robot</a>, welche sich vermutlich auch auf dem 
gleichen Weg loesen lassen. Ich waere hier fuer einen Hinweis dankbar 
(Mail weiter unten).
</p>

