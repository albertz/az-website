<p>
<b>Lazarus</b><br>
Lazarus ist eine freie Open Source RAD-Entwicklungsumgebung auf der Basis
von Free Pascal, ein freier Open Source Pascal Compiler. Ein weiterer Vorteil
von Free Pascal / Lazarus ist die Plattformunabhaengigkeit der damit
erstellten Programme (und auch des Systems selber).<br>
Link: <a href="http://www.lazarus.freepascal.org/" target="_blank">
www.lazarus.freepascal.org</a>
</p>

