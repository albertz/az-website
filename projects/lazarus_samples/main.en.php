<p>
<b>Lazarus</b><br>
Lazarus is a free open source RAD-IDE on the base of Free Pascal, a free
open source pascal compiler. Another advantage of Free Pascal / Lazarus is
the platform-indepence of the compiled programs and of the IDE.<br>
Link: <a href="http://www.lazarus.freepascal.org/" target="_blank">
www.lazarus.freepascal.org</a>
</p>

